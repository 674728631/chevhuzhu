package com.zccbh.demand.pojo.common;

import com.zccbh.demand.pojo.BaseModel;

import java.math.BigDecimal;
import java.util.Date;

/**
 * @Comments:              互助金
 * @Author:                LiangJie
 * @Create Date:           2018年2月28日
 */
public class Foundation extends BaseModel {
    private static final long serialVersionUID = -5079205692396708411L;

    /**
     * 互助金总额
     */
    private BigDecimal amtTotal;
    /**
     * 互助金余额
     */
    private BigDecimal amtBalance;
    /**
     * 已支出金额
     */
    private BigDecimal amtPaid;
    /**
     * 实际支出
     */
    private BigDecimal realPaid;
    /**
     * 虚拟补贴
     */
    private BigDecimal allowance;
    /**
     * 前台展示金额
     */
    private BigDecimal showTotal;
    private int showCustomer;
    private int showCar;
    private int payNum;
    private int eventNum;
    private Long versions;
    /**
     * 创建时间
     */
    private Date createAt;

    public BigDecimal getAmtTotal() {
        return amtTotal;
    }

    public void setAmtTotal(BigDecimal amtTotal) {
        this.amtTotal = amtTotal;
    }

    public BigDecimal getAmtBalance() {
        return amtBalance;
    }

    public void setAmtBalance(BigDecimal amtBalance) {
        this.amtBalance = amtBalance;
    }
    
    public BigDecimal getShowTotal() {
		return showTotal;
	}

	public void setShowTotal(BigDecimal showTotal) {
		this.showTotal = showTotal;
	}

	public int getShowCustomer() {
		return showCustomer;
	}

	public void setShowCustomer(int showCustomer) {
		this.showCustomer = showCustomer;
	}

	public int getShowCar() {
		return showCar;
	}

	public void setShowCar(int showCar) {
		this.showCar = showCar;
	}

	public BigDecimal getAmtPaid() {
        return amtPaid;
    }

    public void setAmtPaid(BigDecimal amtPaid) {
        this.amtPaid = amtPaid;
    }

    public Date getCreateAt() {
        return createAt;
    }

    public void setCreateAt(Date createAt) {
        this.createAt = createAt;
    }

    public BigDecimal getRealPaid() {
        return realPaid;
    }

    public void setRealPaid(BigDecimal realPaid) {
        this.realPaid = realPaid;
    }

    public BigDecimal getAllowance() {
        return allowance;
    }

    public void setAllowance(BigDecimal allowance) {
        this.allowance = allowance;
    }

    public int getPayNum() {
        return payNum;
    }

    public void setPayNum(int payNum) {
        this.payNum = payNum;
    }

    public int getEventNum() {
        return eventNum;
    }

    public void setEventNum(int eventNum) {
        this.eventNum = eventNum;
    }

    public Long getVersions() {
        return versions;
    }

    public void setVersions(Long versions) {
        this.versions = versions;
    }

    @Override
    public String toString() {
        return "Foundation{" +
                "amtTotal=" + amtTotal +
                ", amtBalance=" + amtBalance +
                ", amtPaid=" + amtPaid +
                ", createAt=" + createAt +
                '}';
    }
}
