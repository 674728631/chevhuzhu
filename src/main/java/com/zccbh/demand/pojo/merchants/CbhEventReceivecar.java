package com.zccbh.demand.pojo.merchants;

import com.zccbh.util.base.DateUtils;

import javax.persistence.*;
import java.util.Date;

@Table(name = "cbh_event_receivecar")
public class CbhEventReceivecar {
    /**
     * 主键
     */
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Integer id;

    /**
     * 互助事件号
     */
    @Column(name = "eventNo")
    private String eventNo;

    /**
     * 接送车方式（车主自送，厂方接车，车主自取，厂方送车）
     */
    private Integer type;

    /**
     * 车主姓名
     */
    @Column(name = "nameCarOwner")
    private String nameCarOwner;

    /**
     * 车主电话
     */
    @Column(name = "telCarOwner")
    private String telCarOwner;

    /**
     * 车牌号
     */
    @Column(name = "licensePlateNumber")
    private String licensePlateNumber;

    /**
     * 接车地点
     */
    private String place;

    /**
     * 经度
     */
    private String longitude;

    /**
     * 纬度
     */
    private String latitude;

    /**
     * 接车人名字
     */
    @Column(name = "nameCarReceive")
    private String nameCarReceive;

    /**
     * 接车人联系电话
     */
    @Column(name = "telCarReceive")
    private String telCarReceive;

    /**
     * 备注
     */
    private String remark;

    /**
     * 预计接(送)车时间
     */
    @Column(name = "timeReceiveCar")
    private Date timeReceiveCar;

    /**
     * 状态（接车还是送车）
     */
    private Integer status;

    /**
     * 创建时间
     */
    @Column(name = "createAt")
    private Date createAt;

    /**
     * 获取主键
     *
     * @return id - 主键
     */
    public Integer getId() {
        return id;
    }

    /**
     * 设置主键
     *
     * @param id 主键
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * 获取互助事件号
     *
     * @return eventNo - 互助事件号
     */
    public String getEventNo() {
        return eventNo;
    }

    /**
     * 设置互助事件号
     *
     * @param eventNo 互助事件号
     */
    public void setEventNo(String eventNo) {
        this.eventNo = eventNo;
    }

    /**
     * 获取接送车方式（车主自送，厂方接车，车主自取，厂方送车）
     *
     * @return type - 接送车方式（车主自送，厂方接车，车主自取，厂方送车）
     */
    public Integer getType() {
        return type;
    }

    /**
     * 设置接送车方式（车主自送，厂方接车，车主自取，厂方送车）
     *
     * @param type 接送车方式（车主自送，厂方接车，车主自取，厂方送车）
     */
    public void setType(Integer type) {
        this.type = type;
    }

    /**
     * 获取车主姓名
     *
     * @return nameCarOwner - 车主姓名
     */
    public String getNameCarOwner() {
        return nameCarOwner;
    }

    /**
     * 设置车主姓名
     *
     * @param nameCarOwner 车主姓名
     */
    public void setNameCarOwner(String nameCarOwner) {
        this.nameCarOwner = nameCarOwner;
    }

    /**
     * 获取车主电话
     *
     * @return telCarOwner - 车主电话
     */
    public String getTelCarOwner() {
        return telCarOwner;
    }

    /**
     * 设置车主电话
     *
     * @param telCarOwner 车主电话
     */
    public void setTelCarOwner(String telCarOwner) {
        this.telCarOwner = telCarOwner;
    }

    /**
     * 获取车牌号
     *
     * @return licensePlateNumber - 车牌号
     */
    public String getLicensePlateNumber() {
        return licensePlateNumber;
    }

    /**
     * 设置车牌号
     *
     * @param licensePlateNumber 车牌号
     */
    public void setLicensePlateNumber(String licensePlateNumber) {
        this.licensePlateNumber = licensePlateNumber;
    }

    /**
     * 获取接车地点
     *
     * @return place - 接车地点
     */
    public String getPlace() {
        return place;
    }

    /**
     * 设置接车地点
     *
     * @param place 接车地点
     */
    public void setPlace(String place) {
        this.place = place;
    }

    /**
     * 获取接车人名字
     *
     * @return nameCarReceive - 接车人名字
     */
    public String getNameCarReceive() {
        return nameCarReceive;
    }

    /**
     * 设置接车人名字
     *
     * @param nameCarReceive 接车人名字
     */
    public void setNameCarReceive(String nameCarReceive) {
        this.nameCarReceive = nameCarReceive;
    }

    /**
     * 获取接车人联系电话
     *
     * @return telCarReceive - 接车人联系电话
     */
    public String getTelCarReceive() {
        return telCarReceive;
    }

    /**
     * 设置接车人联系电话
     *
     * @param telCarReceive 接车人联系电话
     */
    public void setTelCarReceive(String telCarReceive) {
        this.telCarReceive = telCarReceive;
    }

    /**
     * 获取备注
     *
     * @return remark - 备注
     */
    public String getRemark() {
        return remark;
    }

    /**
     * 设置备注
     *
     * @param remark 备注
     */
    public void setRemark(String remark) {
        this.remark = remark;
    }

    /**
     * 获取预计接(送)车时间
     *
     * @return timeReceiveCar - 预计接(送)车时间
     */
    public Date getTimeReceiveCar() {
        return timeReceiveCar;
    }

    /**
     * 设置预计接(送)车时间
     *
     * @param timeReceiveCar 预计接(送)车时间
     */
    public void setTimeReceiveCar(Date timeReceiveCar) {
        this.timeReceiveCar = timeReceiveCar;
    }

    /**
     * 获取状态（接车还是送车）
     *
     * @return status - 状态（接车还是送车）
     */
    public Integer getStatus() {
        return status;
    }

    /**
     * 设置状态（接车还是送车）
     *
     * @param status 状态（接车还是送车）
     */
    public void setStatus(Integer status) {
        this.status = status;
    }

    /**
     * 获取创建时间
     *
     * @return createAt - 创建时间
     */
    public Date getCreateAt() {
        return createAt;
    }

    /**
     * 设置创建时间
     *
     * @param createAt 创建时间
     */
    public void setCreateAt(Date createAt) {
        this.createAt = createAt;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }
}