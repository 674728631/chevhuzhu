package com.zccbh.demand.pojo.user;

import com.zccbh.demand.pojo.BaseModel;

import java.util.Date;

/**
 * @Comments:              维修厂员工
 * @Author:                LiangJie
 * @Create Date:           2018年2月28日
 */
public class MaintenanceshopEmployee extends BaseModel {
    private static final long serialVersionUID = 2950631669598321686L;

    /**
     * 维修厂id
     */
    private Integer maintenanceshopId;
    /**
     * 工种类型
     */
    private Integer type;
    /**
     * 维修技师名字
     */
    private String name;
    /**
     * 从业年限
     */
    private Integer workYear;
    /**
     * 简介
     */
    private String introduction;
    /**
     * 维修技师照片
     */
    private String img;
    /**
     * 评价
     */
    private String comment;
    /**
     * 加入时间
     */
    private Date timeJoin;

    public Integer getMaintenanceshopId() {
        return maintenanceshopId;
    }

    public void setMaintenanceshopId(Integer maintenanceshopId) {
        this.maintenanceshopId = maintenanceshopId;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getWorkYear() {
        return workYear;
    }

    public void setWorkYear(Integer workYear) {
        this.workYear = workYear;
    }

    public String getIntroduction() {
        return introduction;
    }

    public void setIntroduction(String introduction) {
        this.introduction = introduction;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public Date getTimeJoin() {
        return timeJoin;
    }

    public void setTimeJoin(Date timeJoin) {
        this.timeJoin = timeJoin;
    }

    @Override
    public String toString() {
        return "MaintenanceshopEmployee{" +
                "maintenanceshopId=" + maintenanceshopId +
                ", type=" + type +
                ", name='" + name + '\'' +
                ", workYear=" + workYear +
                ", introduction='" + introduction + '\'' +
                ", img='" + img + '\'' +
                ", comment='" + comment + '\'' +
                ", timeJoin=" + timeJoin +
                '}';
    }
}
