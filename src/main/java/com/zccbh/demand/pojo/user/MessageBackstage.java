package com.zccbh.demand.pojo.user;

import com.zccbh.demand.pojo.BaseModel;

import java.util.Date;

/**
 * @Comments:              消息通知
 * @Author:                LiangJie
 * @Create Date:           2018年2月28日
 */
public class MessageBackstage extends BaseModel {
    private static final long serialVersionUID = 3765713975366709016L;
    /**
     * 用户id
     */
    private Integer adminId;
    /**
     * 互助类型
     */
    private Integer type;
    /**
     * 编号
     */
    private String orderNo;
    /**
     * 标题
     */
    private String title;
    /**
     * 内容
     */
    private String content;
    /**
     * 状态 1.未读 3已读
     */
    private Integer isSolve;
    /**
     * 创建时间
     */
    private Date createTime;

    public Integer getAdminId() {
        return adminId;
    }

    public void setAdminId(Integer adminId) {
        this.adminId = adminId;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public String getOrderNo() {
        return orderNo;
    }

    public void setOrderNo(String orderNo) {
        this.orderNo = orderNo;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public Integer getIsSolve() {
        return isSolve;
    }

    public void setIsSolve(Integer isSolve) {
        this.isSolve = isSolve;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    @Override
    public String toString() {
        return "MessageBackstage{" +
                "adminId=" + adminId +
                ", type=" + type +
                ", orderNo='" + orderNo + '\'' +
                ", title='" + title + '\'' +
                ", content='" + content + '\'' +
                ", isSolve=" + isSolve +
                ", createTime=" + createTime +
                '}';
    }
}
