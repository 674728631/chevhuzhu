package com.zccbh.demand.pojo.event;

import com.zccbh.demand.pojo.BaseModel;

import java.util.Date;

/**
 * @Comments:              互助事件维修详情
 * @Author:                LiangJie
 * @Create Date:           2018年2月28日
 */
public class EventRepair extends BaseModel {
    private static final long serialVersionUID = 5658482177396395604L;

    /**
     * 互助事件号
     */
    private String eventNo;
    /**
     * 维修人员id
     */
    private Integer repairmanId;
    /**
     * 维修说明
     */
    private String description;
    /**
     * 维修后照片
     */
    private String img;
    /**
     * 开始维修时间
     */
    private Date timeBegin;
    /**
     * 预计结束时间
     */
    private Date timeEnd;

    public String getEventNo() {
        return eventNo;
    }

    public void setEventNo(String eventNo) {
        this.eventNo = eventNo;
    }

    public Integer getRepairmanId() {
        return repairmanId;
    }

    public void setRepairmanId(Integer repairmanId) {
        this.repairmanId = repairmanId;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public Date getTimeBegin() {
        return timeBegin;
    }

    public void setTimeBegin(Date timeBegin) {
        this.timeBegin = timeBegin;
    }

    public Date getTimeEnd() {
        return timeEnd;
    }

    public void setTimeEnd(Date timeEnd) {
        this.timeEnd = timeEnd;
    }

    @Override
    public String toString() {
        return "EventRepair{" +
                "eventNo='" + eventNo + '\'' +
                ", repairmanId=" + repairmanId +
                ", description='" + description + '\'' +
                ", img='" + img + '\'' +
                ", timeBegin=" + timeBegin +
                ", timeEnd=" + timeEnd +
                '}';
    }
}
